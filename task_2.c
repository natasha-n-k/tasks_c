/*
Написать программу, осуществляющую перестановку крайних цифр в целом трехзначном числе.
Целое трёхзначное число задаётся на стандартном потоке stdin, 
результат перестановки выводить в виде трёхзначного числа с учётом ведущих нулей
на стандартный поток вывода stdout. В конце ответа не должно быть переноса.
вход: 159 результат: 951
вход: 120 результат: 021
вход: -123 результат: -321
*/

#include <stdio.h>

int main() {
    int num;
    scanf("%d", &num);

    // Сохраняем знак числа
    int sign = (num < 0) ? -1 : 1;
    num = num * sign;  // Работаем с положительным числом

    // Извлекаем первую, среднюю и последнюю цифры
    int first_digit = num / 100;
    int middle_digit = (num / 10) % 10;
    int last_digit = num % 10;

    // Формируем новое число с переставленными крайними цифрами
    int new_num = last_digit * 100 + middle_digit * 10 + first_digit;

    // Восстанавливаем знак и выводим результат
    if (sign == -1) {
        printf("-%03d", new_num);
    } else {
        printf("%03d", new_num);
    }

    return 0;
}
