/*
ввод не ограничен количеством целых чисел
на вывод: произведение нечетных чисел, если всего 2 числа  - выводить ноль, если одно число - вывести его (но положительным)
ввод 	  | вывод
--------------------
1 2 3 4   | 3
--------------------
2 4         | 0
--------------------
-3    	   | 3
*/

#include <stdio.h>

int main() {
    int num = 0;
    int odd_count = 0; 
    int product = 1; 

    while (scanf("%d", &num) != EOF) {
        if (num % 2 != 0) { // Проверка на нечетное число
            odd_count++;
            product *= (num < 0) ? -num : num; // Берем модуль числа
        }
    }

    if (odd_count == 1) {
        printf("%d", product);  // Если введено одно нечетное число
    } else if (odd_count > 1) {
        printf("%d", product);  // Если введено несколько нечетных чисел
    } else {
        printf("0");  // Если нечетных чисел нет или их только два
    }

    return 0;
}
