/*
Дают число 123
Найти наибольшую цифру, то есть 3
----------
11111
1
18
8
-----------
*/

#include <stdio.h>

int main() {
    int num;
    int max_digit = 0;

    // Считываем число
    scanf("%d", &num);

    // Преобразуем число в положительное (если оно отрицательное)
    if (num < 0) {
        num = -num;
    }

    // Проходим по всем цифрам числа
    while (num > 0) {
        int digit = num % 10;  // Берем последнюю цифру числа
        if (digit > max_digit) {
            max_digit = digit;  // Обновляем максимальную цифру
        }
        num /= 10;  // Убираем последнюю цифру
    }

    // Выводим наибольшую цифру
    printf("%d", max_digit);

    return 0;
}
