/*
Поступают числа x, y и z каждое из которых может принимать одно из двух значений (0 и 1). 
Нужно вывести единицу, если значение следующего выражения истинно: x and (z or y), в противном случае вывести 0. В конце ответа не должно быть переноса на новую строку. 
Проверить на ввод 1 и 0, в случае ошибки вывести "n/a".
входные данные: 111 результат: 1
вход: 000 результат: 0
*/

#include <stdio.h>

int main() {
    int x, y, z;
    int result = 0;

    // Считываем три значения
    if (scanf("%1d%1d%1d", &x, &y, &z) == 3) {
        // Проверяем, что введены только 0 или 1
        if ((x == 0 || x == 1) && (y == 0 || y == 1) && (z == 0 || z == 1)) {
            // Вычисляем выражение x && (z || y)
            result = x && (z || y);
        } else {
            printf("n/a");
            return 0;
        }
    } else {
        printf("n/a");
        return 0;
    }

    printf("%d", result);

    return 0;
}
